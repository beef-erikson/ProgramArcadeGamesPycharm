# Print hi 10 times
for i in range(10):
    print("hi")
print()

# Print hello 5 times and there once
for i in range(5):
    print('hello')
print('there')
print()

# Print hello there 5 times
for i in range(5):
    print('hello')
    print('there')
print()

# Print the numbers 0 to 9
for i in range(10):
    print(i)
print()

# Two ways to print the numbers 1 to 10
for i in range(1, 11):
    print(i)
print()

for i in range(10):
    print(i + 1)
print()

# Two ways to print even numbers 2 to 10
for i in range(2, 10, 2):
    print(i)
print()

for i in range(5):
    print((i + 1) * 2)
print()

# Count down from 10 to 1
for i in range(10, 0, -1):
    print(i)
print()

# Print numbers out of list
for i in [2, 5, 6, 6, 6, 4, 3, 2]:
    print(i)
print()

# Add to self 0 - 9
a = 0
for i in range(10):
    a += i
print(a)
